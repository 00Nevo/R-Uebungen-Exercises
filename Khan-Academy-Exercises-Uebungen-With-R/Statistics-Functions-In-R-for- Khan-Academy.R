##Calculate Arithmetic Mean
 numbers <- c(3,5,6,7,9,8)
> mean_value <- mean(numbers)
> print(mean_value)
[1] 6.333333

##Calculate Median Value
> numbers <- c(4,9,6,3,4,2)
> median_value <- median (numbers)
> print(median_value)
[1] 4

> numbers <- c(9,8,1,8,5)
> median_value <- median (numbers)
> print(median_value)
[1] 8
